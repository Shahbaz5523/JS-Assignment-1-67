function convertDistance() {
    let distnaceIn_KM = 50;
    let distnaceIn_Meters = inMeters(distnaceIn_KM);
    let distnaceIn_Feets = inFeets(distnaceIn_KM);
    let distnaceIn_Inches = inInch(distnaceIn_KM);
    let distnaceIn_Centimeters = inCentimeters(distnaceIn_KM);
    alert("Distance in Meters = " + distnaceIn_Meters);
    alert("Distance in Feets = " + distnaceIn_Feets);
    alert("Distance in Inches = " + distnaceIn_Inches);
    alert("Distance in Centemetrs = " + distnaceIn_Centimeters);
}
function inMeters(d) {
    let oneKM = 1000 // one kilometer = 1000 meter
    return d * oneKM;
}
function inFeets(d) {
    let oneKM = 3280.84; // one kilometer = 3280.84 feets
    return d * oneKM;
}
function inInch(d) {
    let oneKM = 39370.08; // one kilometer = 39370.08 inchs
    return d * oneKM;
}
function inCentimeters(d) {
    let oneKM = 100000; // one kilometer = 100000 centimeters
    return d * oneKM;
}

